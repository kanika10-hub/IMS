CREATE OR REPLACE PROCEDURE DEMOUSER.PROCESS_STOCK_MOVEMENT(
ITEM_ID IN NUMBER,
Qty_Change IN NUMBER,
Trans_Type IN VARCHAR2
)
IS
BEGIN
	UPDATE DEMOUSER.FACTINVENTORY 
		SET QUANTITY_ON_HAND = QUANTITY_ON_HAND + Qty_Change ;
		
	INSERT INTO DEMOUSER.FACTINVENTORY_TRANSACTIONS (TRANSID, ITEM_ID, QUANTITY, TRANS_TYPE, DATE_CREATED)
	VALUES(0, ITEM_ID, Qty_Change, Trans_Type, sysdate);
COMMIT;
EXCEPTION
	WHEN OTHERS THEN 
		ROLLBACK;
END PROCESS_STOCK_MOVEMENT;
