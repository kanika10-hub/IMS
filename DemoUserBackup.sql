CREATE OR REPLACE FORCE EDITIONABLE VIEW "DEMOUSER"."GET_LOW_STOCK_ALERTS" ("ITEM_NAME", "QUANTITY_ON_HAND", "MIN_STOCK") AS 
  SELECT item.ITEM_NAME, inventory.QUANTITY_ON_HAND, inventory.MIN_STOCK  FROM 
DEMOUSER.FACTINVENTORY inventory JOIN DEMOUSER."DimItem" item 
ON inventory.ITEM_ID = item.ITEM_ID
WHERE inventory.QUANTITY_ON_HAND < inventory.MIN_STOCK ;
;

